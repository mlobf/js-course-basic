let x = 10;
let y = 12;

console.log(x + y);
console.log(x - y);
console.log(x / y);
console.log(x * y);
console.log(x % y);
console.log(x ** y);

//Increment (++)
console.log(x++);
console.log(x);

//Decrement
console.log(--x);

//Increment with add.
let z = 5;
console.log(z += 5);
console.log(z -= 5);
console.log(z *= 5);
console.log(z /= 5);
console.log(z **= 5);
console.log(z %= 5);

//Comparison Operator. 
let h = 1;

//Relation Operators.
console.log(h > 0);
console.log(h >= 0);
console.log(h < 0);
console.log(h <= 0);

//Equality Operators.
console.log(h === 1);
console.log(h !== 1);

//Strict Equality. => (Type + Value)
console.log(1 === 1);
console.log('1' === 1);
//Lose Equality. => (Type ! Value)
console.log("Lose Equality")
console.log(1 == 1);
console.log('1' == 1);
console.log(true == 1);